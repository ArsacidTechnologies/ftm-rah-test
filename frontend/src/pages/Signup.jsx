import React, { useState } from 'react';
import axios from 'axios';

const Signup = () => {
  const [username, setUsername] = useState('');
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');

  const handleSubmit = async (e) => {
    e.preventDefault();

    try {
      const response = await axios.post('http://localhost:4000/signup', {
        username,
        email,
        password,
      });

      if (response.data.isSuccessful) { // Changed 'success' to 'isSuccessful'
        alert('Signup successful! Your token: ' + response.data.authToken); // Changed 'token' to 'authToken'
      } else {
        alert('Signup failed: ' + response.data.message); // Changed 'errors' to 'message'
      }
    } catch (error) {
      // Display detailed error message from the server or a general error message
      alert('An error occurred during signup: ' + (error.response?.data?.message || error.message));
      console.error('Signup error:', error);
    }
  };

  return (
    <div>
      <h2>Signup</h2>
      <form onSubmit={handleSubmit}>
        <div>
          <label>Username:</label>
          <input
            type="text"
            value={username}
            onChange={(e) => setUsername(e.target.value)}
            required
          />
        </div>
        <div>
          <label>Email:</label>
          <input
            type="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            required
          />
        </div>
        <div>
          <label>Password:</label>
          <input
            type="password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            required
          />
        </div>
        <button type="submit">Signup</button>
      </form>
    </div>
  );
};

export default Signup;



